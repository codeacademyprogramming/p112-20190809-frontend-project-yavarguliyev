"use strict";

// Canvas Menu

function openNav() {
    document.getElementById("mySidenav").style.width = "360px";
    document.getElementById("main").style.marginLeft = "360px";
  }
  
  function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("main").style.marginLeft= "0";
  }